public class Calculation {

    static int addition(int num1, int num2) {// 加算
		return num1 + num2;
	}

	static int subtraction(int num1, int num2) {// 減算
		return num1 - num2;
	}

	static int multiplication(int num1, int num2) {// 乗算
		return num1 * num2;
	}

	static int division(int num1, int num2) {// 除算
		return num1 / num2;
	}

	static int remainder(int num1, int num2) {// 剰余
		return num1 % num2;
	}
}
